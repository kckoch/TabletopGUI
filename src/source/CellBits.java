package source;

public enum CellBits {
    NOTHING(0x00000000), BLOCKED(0x00000001), ROOM(0x00000002), CORRIDOR(0x00000004), 
    PERIMETER(0x00000010), ENTRANCE(0x00000020), ROOM_ID(0x0000FFC0), ARCH(0x00010000), 
    DOOR(0x00020000), LOCKED(0x00040000), TRAPPED(0x00080000), SECRET(0x00100000), 
    PORTC(0x00200000), STAIR_DN(0x00400000), STAIR_UP(0x00800000), LABEL(0xFF000000),
    OPENSPACE(0x00000002|0x00000004), DOORSPACE(0x00010000|0x00020000|0x00040000|0x00080000|0x00100000|0x00200000),
    ESPACE(0x00000020|0x00010000|0x00020000|0x00040000|0x00080000|0x00100000|0x00200000|0xFF000000),
    STAIRS(0x00400000|0x00800000), BLOCK_ROOM(0x00000001|0x00000002), BLOCK_CORR(0x00000001|0x00000010|0x00000004),
    BLOCK_DOOR(0x00000001|0x00010000|0x00020000|0x00040000|0x00080000|0x00100000|0x00200000);

    private final int code;
    CellBits(int i) {
        code = i;
    }
    public int getCode() {
        return code;
    }
}
